package Test1ATB13x;

public class Question4 {
    public static void main(String[] args) {
        int a =5;
        System.out.printf("Original: %d",a);
        System.out.println();
        System.out.printf("Pre-increment: %d",++a);
        System.out.println();
        System.out.printf("Post-increment: %d",a++);
        System.out.println();
        System.out.printf("Pre-decrement: %d",--a);
        System.out.println();
        System.out.printf("Post-decrement: %d",a--);

    }
}
